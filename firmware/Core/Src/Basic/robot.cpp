#include "Basic/robot.h"

#include "Buzzer/buzzer.hpp"

#include <array>

////////////////////////////////////////////////////////////////////////////////
// Subsystems.
////////////////////////////////////////////////////////////////////////////////

static Buzzer buzzer;

static constexpr std::array<Subsystem*, 1> subsystems = {
    &buzzer,
};

////////////////////////////////////////////////////////////////////////////////
// Robot functions.
////////////////////////////////////////////////////////////////////////////////

void Robot_Init() {
  for (auto s : subsystems) {
    s->init();
  }
}

void Robot_Update() {
  for (auto s : subsystems) {
    s->process();
  }
}
